private void loadLecturerData()throws SQLClientInfoException{
       List<Lecturer> lecturer = controller.getLecturer();
       String[][] data = new String[lecturer.size()][4];
       
       for (int i=0; i<lecturer.size(); i++){
           data [i][0] = String.valueOf(lecturer.get(i).getIDDosen());
           data [i][1] = lecturer.get(i).getName();
           data [i][2] = lecturer.get(i).getnidn();
           data [i][3] = lecturer.get(i).getKeahlian();
       }
       String[] columnName = {"IDDosen","Name","nidn","Keahlian"};
       jTable1.setModel(new javax.swing.table.DefaultTableModel(data,columnName));
       
    }
