    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
 // TODO add your handling code here:
     
    int selectedRow =jTable1.getSelectedRow();
        if(selectedRow !=-1){
            jTextFieldnidn.setEditable(false);
            selectednidn = jTable1.getValueAt(selectedRow, 2).toString();
            jTextFieldIDDosen.setText(jTable1.getValueAt(selectedRow,0).toString());
            jTextFieldNamaDosen.setText(jTable1.getValueAt(selectedRow,1).toString());
            jTextFieldnidn.setText(jTable1.getValueAt(selectedRow, 2).toString());
            jComboBoxKeahlian.setSelectedItem(jTable1.getValueAt(selectedRow, 3).toString());
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed

        // TODO add your handling code here:
        String name = jTextFieldNamaDosen.getText();
        String IDDosen = jTextFieldIDDosen.getText();
        String nidn = jTextFieldnidn.getText();
        String Keahlian = comboBoxModelkeahlian.getSelectedItem().toString();

        if(name.isEmpty() || IDDosen.isEmpty() || nidn.isEmpty() || Keahlian.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please fill in all fields", "Input Eror", JOptionPane.ERROR_MESSAGE);
        }else {
            Lecturer lecturer  = new Lecturer(IDDosen, name,nidn,Keahlian);
            try {
                int res;
                if(selectednidn != null){
                    res = controller.update(lecturer, selectednidn);
                }else {
                    res = controller.create(lecturer);
                }
                if (res == 1) {
                    JOptionPane.showMessageDialog(this, "User Created Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                    this.clearForm();
                    loadLecturerData();
                }else {
                    JOptionPane.showMessageDialog(this, "Eror occurred while inserting", "Eror", JOptionPane.ERROR_MESSAGE);
                }
            }catch (SQLException ex){
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Eror", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonSimpanActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this Lecturer?", "Delete Confirmation", JOptionPane.YES_NO_OPTION);
        if(confirm == JOptionPane.YES_OPTION){
            try {
                controller.delete(selectednidn);
                JOptionPane.showMessageDialog(this, "Lecturer delete succesfully!");
                this.selectednidn = null;
                this.clearForm();
                loadLecturerData();
            }catch (SQLException ex){
                Logger.getLogger(LecturerForm.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "delete unsuccesfully!");
            }
        }          // TODO add your handling code here:
    }//GEN-LAST:event_jButtonDeleteActionPerformed

