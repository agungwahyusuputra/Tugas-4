    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
        // TODO add your handling code here:
        String name = jTextFieldname.getText();
        String code = jTextFieldCode.getText();
        String sksText = jTextFieldSKS.getText();
        String semesterText = jComboBoxSmstr.getSelectedItem().toString();

if (name.isEmpty() || code.isEmpty() || sksText.isEmpty() || semesterText.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Please fill in all fields", "Input Error", JOptionPane.ERROR_MESSAGE);
} else {
    int sks = Integer.parseInt(sksText);
    int semester = Integer.parseInt(semesterText);

    Course course = new Course(code, code, name, sks, semester);
    // lanjut controller.create() atau update()

            try {
                int res;
                 
                if(selectedcode != null){
                res = controller.update(course, selectedcode);
                    }else{
                res = controller.create(course);
                }
                if (res == 1) {
                    JOptionPane.showMessageDialog(this, "User Created Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                    this.clearForm();
                    loadCourseData();
                }else {
                    JOptionPane.showMessageDialog(this, "Eror occurred while inserting", "Eror", JOptionPane.ERROR_MESSAGE);
                }
            }catch (SQLException ex){
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Eror", JOptionPane.ERROR_MESSAGE);
            }
       }
    }//GEN-LAST:event_jButtonSimpanActionPerformed

