    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
       
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this student?", "Delete Confirmation", JOptionPane.YES_NO_OPTION);
        if(confirm == JOptionPane.YES_OPTION){
            try {
                controller.delete(selectedcode);
                JOptionPane.showMessageDialog(this, "Student delete succesfully!");
                this.selectedcode = null;
                this.clearForm();
                loadCourseData();
            }catch (SQLException ex){
                Logger.getLogger(CourseForm.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "delete unsuccesfully!");
            }
        }   // TODO a
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    
    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int selectedRow =jTable1.getSelectedRow();
        if(selectedRow >=0){
            jTextFieldCode.setEditable(false);
            selectedcode = jTable1.getValueAt(selectedRow, 0).toString();
            jTextFieldCode.setText(jTable1.getValueAt(selectedRow,0).toString());
            jTextFieldname.setText(jTable1.getValueAt(selectedRow,1).toString());
            jTextFieldSKS.setText(jTable1.getValueAt(selectedRow, 2).toString());
            jComboBoxSmstr.setSelectedItem(jTable1.getValueAt(selectedRow, 3).toString());
        }
    }//GEN-LAST:event_jTable1MouseClicked

