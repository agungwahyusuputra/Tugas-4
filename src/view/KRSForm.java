    private void jButtonSIMPANActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSIMPANActionPerformed
        // TODO add your handling code here:
        Course course = (Course)jComboBoxCodeMK.getSelectedItem();
        Student student = (Student)jComboBoxNimMhs.getSelectedItem();
        Lecturer lecturer = (Lecturer)jComboBoxnidn.getSelectedItem();
        
        String scoreText = jTextFieldScore.getText();
        if(course == null || lecturer == null || scoreText.isEmpty()){
            
            JOptionPane.showMessageDialog(this,
                    "Please fill in all fields",
                    "Inpur Eror",
                    JOptionPane.ERROR_MESSAGE);
        }else{
             double score = Double.parseDouble(scoreText);
             
             int semester = course.getsemester();
             
             KRS krs = new KRS(course, score, "", lecturer, semester, student);
             jLabelgrade.setText(krs.getGrade());
             try{

            int res;

            
            if(selectedNIM != null){
                res = kRSController.update(krs, selectedNIM);
            }else{
                res = kRSController.create(krs);
            }

            if(res == 1){

                JOptionPane.showMessageDialog(this,
                        "KRS Saved Successfully",
                        "Success",
                        JOptionPane.INFORMATION_MESSAGE);

                clearForm();
                loadKrsData();

            }else{

                JOptionPane.showMessageDialog(this,
                        "Error occurred while saving data",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
            }

        }catch(SQLException ex){

            JOptionPane.showMessageDialog(this,
                    ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }
        
    }//GEN-LAST:event_jButtonSIMPANActionPerformed

