        private void loadKrsData() throws SQLException{
            List<KRS> krs = kRSController.getKrs();
            String[][] data = new String[krs.size()][6];
        
        for (int i=0; i < krs.size(); i++){
            data[i][0] = krs.get(i).getcourse().getCourseName();
            data[i][1] = krs.get(i).getstudent().getName();
            data[i][2] = krs.get(i).getLecturer().getName();
            data[i][3] = String.valueOf(krs.get(i).getsemester());
            data[i][4] = String.valueOf(krs.get(i).getScore());
            data[i][5] = krs.get(i).getGrade();
        }
        String[] columnName = {"Matakuliah","Mahasiswa","Dosen","Semester", "Score", "Grade"};
        jTable1.setModel(new javax.swing.table.DefaultTableModel(data, columnName));
    }
